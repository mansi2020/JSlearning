/* calculation of Number */
function calValue(OP1,Operand,OP2){
	
	switch(Operand){
		case '+':
			return OP1+OP2;

		
		case '-':
			return OP1-OP2;
			
		case '*':
			return OP1*OP2;
			
		case '/':
			return OP1/OP2;
		
			} 
	
}

console.log(calValue(13,'+',12));

console.log(calValue(13,'-',12));

console.log(calValue(12,'*',12));

console.log(calValue(13,'/',12));




	
	
	
	
	
		
		
		
 











